        f"- –ú–æ–¥–µ–ª—å: {WHISPER_MODEL if SPEECH_RECOGNITION_ENGINE.lower() == 'whisper' else '–°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∞—è'}\n\n"
