        f"—Ç–µ—Ö–Ω–æ–ª–æ–≥–∏—é —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞–Ω–∏—è —Ä–µ—á–∏ {SPEECH_RECOGNITION_ENGINE.capitalize()}.\n\n"
        "üîß *–¢–µ—Ö–Ω–∏—á–µ—Å–∫–∏–µ –¥–µ—Ç–∞–ª–∏:*\n"
